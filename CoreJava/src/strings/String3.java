package strings;

public class String3 {

	public static void main(String[] args) {
		//declare a string array...
		String s[] = {"Jince","Biju","Mahesh","Selenium","London"};
        
		System.out.println(s.length);  //fetches the length of the array..
		System.out.println(s[3]);     //prints the 3rd element
		System.out.println(s[3].length());  //fetches the length of the 3rd element...
	}

}
